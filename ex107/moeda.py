def aumentar(n, aut):
    aumentado = n + (n * aut / 100)
    return aumentado


def diminuir(n, dim):
    dimi = n - (n * dim / 100)
    return dimi


def dobro(n):
    dob = n * 2
    return dob


def metade(n):
    meta = n / 2
    return meta

